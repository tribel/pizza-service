package ua.rd.pizza_service.domain;

public class Customer {

	private long id;
	private String name;

	public Customer() {
	}

	public Customer(long id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
